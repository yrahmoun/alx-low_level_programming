#include "3-calc.h"

/**
 * op_add - adds
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates modulo
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
